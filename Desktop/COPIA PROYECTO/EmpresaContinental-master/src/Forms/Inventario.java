    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int filaselec = jTableInventario.getSelectedRow();
        if (filaselec >= 0) {
            JOptionPane.showMessageDialog(null, "esta seguro de eliminar este articulo?.");
            modelo.removeRow(filaselec);
            Inventarios delete = new Inventarios();
            try {
                delete.eliminar(filaselec);
                JOptionPane.showMessageDialog(null, "Articulo del inventario eliminado de forma permanente.");
            } catch (SQLException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una casilla.");
            jPanel2.setVisible(false);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

