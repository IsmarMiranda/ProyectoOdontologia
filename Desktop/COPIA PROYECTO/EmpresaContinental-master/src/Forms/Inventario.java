    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Inventarios enviar = new Inventarios();
        if (modifier == 0 && filaSeleccionada == 0) {
            String Dato[] = new String[6];
            Dato[0] = String.valueOf(cont + 1);
            Dato[1] = txtNombre.getText();
            Dato[2] = txtPrecio.getText();
            Dato[3] = txtExistencia.getText();
            Dato[4] = String.valueOf(Double.parseDouble(txtPrecio.getText()) * Integer.parseInt(txtExistencia.getText()));

            JOptionPane.showMessageDialog(null, "Articulo ingresado exitosamente!");
            jPanel2.setVisible(false);
            modelo.addRow(Dato);
            enviar.insertar(txtNombre.getText(), Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencia.getText()));
            txtNombre.setText("");
            txtPrecio.setText("");
            txtExistencia.setText("");

            cont++;
        } else {
            int filaselect = jTableInventario.getSelectedRow();
            try {
                enviar.update(txtNombre.getText(), Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencia.getText()), Integer.parseInt(jTableInventario.getValueAt(filaselect, 0).toString()));
            } catch (SQLException ex) {
                Logger.getLogger(Inventario.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Articulo modificado exitosamente!");
            jTableInventario.setValueAt(txtNombre.getText(), filaselect, 1);
            jTableInventario.setValueAt(txtPrecio.getText(), filaselect, 2);
            jTableInventario.setValueAt(txtExistencia.getText(), filaselect, 3);
            jTableInventario.setValueAt(String.valueOf(Double.parseDouble(txtPrecio.getText()) * Integer.parseInt(txtExistencia.getText())), filaselect, 4);
            jPanel2.setVisible(false);
            txtNombre.setText("");
            txtPrecio.setText("");
            txtExistencia.setText("");
            filaSeleccionada = 0;
            modifier = 0;
        }
        //  System.out.println("SE HA ENVIADO A LA DATA BASE");
    }//GEN-LAST:event_btnAceptarActionPerformed


